unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int size_src;
	unsigned int i;

	size_src = 0;
	i = 0;
	while (src[size_src] != '\0')
		size_src++;
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (size_src);
}
