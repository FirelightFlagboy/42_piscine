int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int count;

	i = -1;
	count = 0;
	while (tab[++i])
	{
		count += ((*f)(tab[i]) == 1) ? 1 : 0;
	}
	return (count);
}
